package vn.datset2.trung95.earn29122018.entities;

public class CommonColumn {
	protected Long id;
	protected int version;
	public CommonColumn() {
		super();
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}
	
	
}
